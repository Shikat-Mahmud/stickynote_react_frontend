// Convert ISO code to flag emoji
const codeToEmoji = (countryCode) => {
  if (!countryCode) return "ðŸ³ï¸";
  return countryCode
    .toUpperCase()
    .replace(/./g, (char) =>
      String.fromCodePoint(127397 + char.charCodeAt())
    );
};

// Component you can use
const CountryFlag = ({ countryName, showName = false }) => {
  const isoCode = countryNameToCode[countryName] || "";
  const emoji = codeToEmoji(isoCode);

  return (
    <span className="inline-flex items-center gap-1">
      <span className="text-lg">{emoji}</span>
      {showName && countryName && <span>{countryName}</span>}
    </span>
  );
};

export default CountryFlag;
